// Initialize a variable 'count' with the value 0
var count = 0;

// Use a while loop to repeat a block of code as long as 'count' is less than 10
while (count < 10) {
    // Print the current value of 'count' to the console
    console.log("Current Count: " + count);
    
    // Increment the value of 'count' by 1
    count++;
}

// Print a message to the console after the loop finishes
console.log("Loop stopped!");
